package com.telran.person.dto;

public class NumberDtoBuilder {
    private   int id;
    private String number;
    private  int personId;

    public NumberDtoBuilder setId(int id) {
        this.id = id;
        return this;
    }

    public NumberDtoBuilder setNumber(String number) {
        this.number = number;
        return this;
    }

    public NumberDtoBuilder setPersonId(int personId) {
        this.personId = personId;
        return this;
    }

    public NumberDto build(){
        return new NumberDto(id,number,personId);
    }
}
